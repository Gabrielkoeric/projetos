    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $data = DB::table('perfil')
            ->select('perfil.nome as perfil_nome', 'home.nome as tela_nome')
            ->join('perfil_permissao', 'perfil.id_perfil', '=', 'perfil_permissao.id_perfil')
            ->join('home', 'perfil_permissao.id_home', '=', 'home.id_home')
            ->orderBy('perfil.nome')
            ->orderBy('home.nome')
            ->get();

        return view('perfil.index')->with('data', $data);
    }

